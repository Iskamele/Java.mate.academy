package section01_JavaBasics.topic03_Operators.theory;

public class AssignmentOperators {
    public static void main(String[] args) {
        int candies = 11;
        candies += 7;
        System.out.println(candies);
        candies -=7;
        System.out.println(candies);
        candies *=7;
        System.out.println(candies);
    }
}
