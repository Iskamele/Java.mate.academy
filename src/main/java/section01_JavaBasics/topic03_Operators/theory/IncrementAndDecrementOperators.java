package section01_JavaBasics.topic03_Operators.theory;

public class IncrementAndDecrementOperators {
    public static void main(String[] args) {
        int count = 20;
        System.out.println(count);
        count = count +1;
        System.out.println(count);
        count +=1;
        System.out.println(count);
        count++;
        System.out.println(count);
        count--;
        System.out.println(count);
    }
}
