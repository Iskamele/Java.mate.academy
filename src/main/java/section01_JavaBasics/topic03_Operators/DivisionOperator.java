package section01_JavaBasics.topic03_Operators;

public class DivisionOperator {
    public static void main(String[] args) {
        double a = 6;
        int b = 4;
        double result = a / b;
        System.out.println(result);

        float a2 = 6;
        int b2 = 4;
        float result2 = a2 / b2;
        System.out.println(result2);
    }
}
