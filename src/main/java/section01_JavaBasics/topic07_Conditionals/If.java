package section01_JavaBasics.topic07_Conditionals;

public class If {
    public static void main(String[] args) {
        int x = 10;

        if (x > 5) {
            System.out.println(1);
            System.out.println(2);
            System.out.println(3);
        }

        System.out.println(x);


    }
}
